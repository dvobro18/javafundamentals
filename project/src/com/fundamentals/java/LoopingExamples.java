package com.fundamentals.java;

public class LoopingExamples {
	public void mywhileloop() {
		int value = 0;
				while(value < 10) {
					System.out.println(value);
					value ++;
				}//end of while loop
	}//end of my method
	
	public void myDowhileloop() {
		int value = 0;
		do {
			System.out.println(value);;
			value++;
		}while(value < 10);
	}//end of method

}//end of my class
